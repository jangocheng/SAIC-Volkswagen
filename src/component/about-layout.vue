	created () {
		$(function () {
	 	//轮播图
	    	var aLen = $('.factory-ul li').length;
	    	var now = 0;
	    	function nextpage () {
	    		now++;
	    		if(now >= (aLen - 2)) {
	    			$('.factory-content .next-layout').off();
	    			$('.factory-content .factory-ul').stop().animate({left: -1300*(now + 1)},function(){
	    				$('.factory-content .next-layout').on('click',nextpage);
	    				$('.factory-content .factory-ul').css('left',-1300);
	    			})
	    			now = 0;
	    		} else {
	    			$('.factory-content .factory-ul').stop().animate({left:-1300 * (now + 1)});
	    		}
	    	}
	    	function previouspage () {
	    	now--;
	    		if(now == -1){
	    			$('.factory-content .page-layout').off();
	    			$('.factory-ul').stop().animate({left:-1300*(now + 1)},function () {
	    				$('.factory-content .page-layout').on('click',previouspage);
	    				  $('.factory-ul').css({left:-1300 * (aLen - 2)});  
	    			})
	    			now = aLen - 3;
	    		} else {
	    			$('.factory-ul').stop().animate({left:-1300*(now+1)});
	    		}
	    		$('.next-layout').css({'textDecoration':'none','color':'#7c8185'});
	    		$('.page-layout').css({'textDecoration':'none','color':'#7c8185'});
	    	}
	    	$('.factory-content .next-layout').on('click',nextpage);
	    	$('.factory-content .page-layout').on('click',previouspage);
	    	//点击显示地址
	    	var aIcon=$('.layout-map a');
