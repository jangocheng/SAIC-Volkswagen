		var disL=0;
	    var ulW=Math.floor($(obj).width());
	    var boxW=Math.floor($('.honor-img1').width());
	    var pageL=0;
	    function nextpage () {
	    	now++;
			disW=Math.floor(aLi.eq(now-1).width());
//			alert(aLi.eq(now-1).width());
			disL=ulW-ulL;
	    	if(disL<=boxW) {	
