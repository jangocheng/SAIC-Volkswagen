				disL=ulW-ulL;
//	    		return;
	    	} else {
//				alert(aLi.eq(now).width());
				ulL+=disW;
   				$(obj).stop().animate({'left':-ulL});
				n++;
	    	}
	    }
	    function previouspage () {
		    now--;
//		    alert(aLi.eq(now).width());
			disW=Math.floor(aLi.eq(now).width());
			ulL-=disW;
			pageL=ulL;
		    if(now <0){
		    	now=0;
		    	disW=0;
		    	n=0;
		    	ulL=0;
		    	disL=0;
		    	$(obj).stop().animate({'left':0});
//		    	return;
		    } else {
		   		$(obj).stop().animate({'left':-ulL});
	    	}
	    }
	    $(next).on('click',nextpage);
	    $(page).on('click',previouspage);
	}
