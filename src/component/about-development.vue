<script>
export default {
	created () {
		function carousel(obj,objli,page,next){
		 var aLen = $(objli).length;
	    	var now = 0;
	    	function nextpage () {
	    		now++;
	    		if(now >= (aLen - 2)) {
	    			$(next).off();
	    			$(obj).stop().animate({left: -1300*(now + 1)},function(){
	    				$(next).on('click',nextpage);
	    				$(obj).css('left',-1300);
	    			})
	    			now = 0;
	    		} else {
	    			$(obj).stop().animate({left:-1300 * (now + 1)});
	    		}
	    	}
	    	function previouspage () {
	    	now--;
	    		if(now == -1){
	    			$(page).off();
	    			$(obj).stop().animate({left:-1300*(now + 1)},function () {
	    				$(page).on('click',previouspage);
	    				  $(obj).css({left:-1300 * (aLen - 2)});  
	    			})
	    			now = aLen - 3;
	    		} else {
	    			$(obj).stop().animate({left:-1300*(now+1)});
	    		}
	    		$(next).css({'textDecoration':'none','color':'#fff'});
	    		$(page).css({'textDecoration':'none','color':'#fff'});
	    	}
	    	$(next).on('click',nextpage);
	    	$(page).on('click',previouspage);
	}
	$(function () {
	   	carousel('.development-ul1','.development-ul1 li','.development-page1','.development-next1');
	   	carousel('.development-ul2','.development-ul2 li','.development-page2','.development-next2');
